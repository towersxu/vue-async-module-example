<template>
  <div class="mask hidden" v-bind:class="{'show':isShow}">
    <div class="loader" v-show="isLoading">Loading...</div>
    <div id="example">
      <async></async>
    </div>
  </div>
</template>

<script>
  module.exports = {
    replace: true,
    data: function () {
      return {
        isShow: false,
        isLoading: true
      }
    },
    methods: {
      reset: function () {
        this.isShow = false;
        this.isLoading = false;
      }
    },
    events: {
      'mask_show': function (msg) {
        this.isShow = true;
        if (!this.asyncModule) {
          var that = this;
          var Vue = require('vue');
          Vue.component('async', function (resolve, reject) {
            require.ensure(["../modal/user-modal.vue"], function (require) {
              that.isLoading = false;
              resolve(require("../modal/user-modal.vue"));
            });
          });
          that.asyncModule = new Vue({
            el: '#example',
            data: function(){
              return {msg:msg};
            },
            events: {
              'get_init': function () {
                this.$broadcast('init_data', this.msg);
              },
              'close_modal': function () {
                that.reset();
              }
            }
          });
        } else {
          this.asyncModule.msg = msg;
          this.asyncModule.$emit('get_init');
        }
      }
    }
  };
</script>

